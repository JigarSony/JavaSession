package finalConcepts;

public class FinalConcept {

	public static void main(String[] args) {
		
		/*
		 * final is keyword
		 * Final keyword is used to define constant value
		 */
		
		final int i = 10; //constant value : value we cannot change/modify
		//it's prevent inheritance
		//i = 20;
		//i = 30;
		System.out.println(i);
	}

}
